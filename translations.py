# دالة الترجمة
def translate(text, lang='ar'):
    """
    ترجمة النص من العربية إلى الإنجليزية أو العكس

    المعلمات:
        text (str): النص المراد ترجمته
        lang (str): لغة العرض ('ar' للعربية، 'en' للإنجليزية)

    العائد:
        str: النص المترجم
    """
    if lang == 'ar':
        return text

    # البحث في قاموس الترجمة
    if text in translations:
        return translations[text]

    # إذا لم يتم العثور على الترجمة، إرجاع النص الأصلي
    return text
